# loops are used to perform a repeated task.
fruits = ["apple", "plum", "grapes"]
for fruit in fruits:
    print(fruit)
    print(fruit + " pie")

# range(). The range function is used to get the range of numbers.EX range(1,11). Get all the numbers from 1 through
# 10 up to 11 but not including 11.
total = 0
for num in range(1, 101):
    total += num
print(total)

